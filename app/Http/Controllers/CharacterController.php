    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $basic_character_details = $request->toArray();
        $basic_character_details['user_id'] = Auth::id();
        $skills = $request->skills;
        $stats = $request->stats;
        $saving_throws = $request->saving_throws;
        unset($basic_character_details['skills']);
        unset($basic_character_details['stats']);
        unset($basic_character_details['saving_throws']);
        $character = Character::create($basic_character_details);
        $character->stats()->create($stats);
        $character->skills()->create($skills);
        $character->saving_throws()->create($saving_throws);
    }

